void	ft_div_mod(int a, int b, int *div, int *mod)
{
	int	div_res;
	int	mod_res;

	if (b != 0)
	{
		div_res = (a / b);
		*div = div_res;
		mod_res = (a % b);
		*mod = mod_res;
	}
}
